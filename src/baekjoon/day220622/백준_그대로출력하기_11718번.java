package baekjoon.day220622;

import java.util.Scanner;

public class 백준_그대로출력하기_11718번 {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        while (sc.hasNext()) {
            String s = sc.nextLine();

            System.out.println(s);
        }

    } // end main
} // end class
