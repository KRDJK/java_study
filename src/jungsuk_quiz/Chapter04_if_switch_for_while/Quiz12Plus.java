package jungsuk_quiz.Chapter04_if_switch_for_while;

public class Quiz12Plus {
    public static void main(String[] args) {

        for (int i = 2; i <= 9; i++) {
            for (int j = 1; j <= 3; j++) {
                System.out.printf("%d*%d=%d\n", i, j, i*j);
            }
            System.out.println();
        }
            
    } // end main
}
